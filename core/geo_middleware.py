def ip_to_int(ip_str: str) -> int:
    try:
        parts = ip_str.split(".")
        if len(parts) != 4:
            return -1
        return (int(parts[0]) << 24) + (int(parts[1]) << 16) + (int(parts[2]) << 8) + int(parts[3])
    except (ValueError, IndexError):
        return -1


def is_russian_ip(ip_str: str) -> bool:
    ip_int = ip_to_int(ip_str)
    if ip_int < 0:
        return False

    left = 0
    right = len(RUSSIAN_IP_RANGES) - 1

    while left <= right:
        mid = (left + right) // 2
        range_start, range_end = RUSSIAN_IP_RANGES[mid]

        if range_start <= ip_int <= range_end:
            return True

        if ip_int < range_start:
            right = mid - 1
        else:
            left = mid + 1

    return False


class GeoRestrictionMiddleware(BaseHTTPMiddleware):
    async def dispatch(self, request: Request, call_next):
        ip = client_ip(request)

        if not is_russian_ip(ip):
            return Response(
                content='{"detail": "Access denied: geographic restriction"}',
                status_code=403,
                media_type="application/json",
                headers={"X-Geo-Restriction": "RU-only"},
            )

        return await call_next(request)
